    /** Creates new form DownloadBox */
    public DownloadBox(String appId, String title, String version) {
        initComponents();
        Image icon = getToolkit().getImage(getClass().getResource("/images/download.png"));
        setIconImage(icon);
        this.appId = appId;
        title = ConvertURL.toUrlFriendly(title).replaceAll(":", " -").replaceAll("/", " - ");
        this.version = version;
        int type = APKDownloaderApp.config.getFormatFileType();
        if (type == 1 || type == 3) {
            this.title = title + ".apk";
        } else {
            this.title = title + " v" + this.version + ".apk";
        }
        lbFilename.setText(this.title);
        startDownload();
    }

    private void startDownload() {
        appDao = new ListAppsDAO();
        download = new Task();
        downloads.add(download);
        this.index = downloads.lastIndexOf(download);
        download.addPropertyChangeListener(new PropertyChangeListener() {

            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                if ("progress".equals(evt.getPropertyName())) {
                    if (download.isCancelled()) {
                        btnCancel.setEnabled(false);
                        setTitle("Загрузка отменена");
                    }
                    int progress = (Integer) evt.getNewValue();
                    if (progress == 100) {
                        setTitle("Загрузка прошла успешно!");
                    }
                    pbDownload.setValue(progress);
                    txtPercent.setText(progress + "%");
                }
            }
        });
        download.execute();
    }

