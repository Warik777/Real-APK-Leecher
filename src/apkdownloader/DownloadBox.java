    public class Task extends SwingWorker<Void, Void> {

        @Override
        public Void doInBackground() {
            setProgress(0);
            appDao.appDownload(appId, title, index);

            return null;
        }

        @Override
        public void done() {
            if (!isCancelled()) {
                Toolkit.getDefaultToolkit().beep();
                btnCancel.setEnabled(false);
            }
        }
    }
    
